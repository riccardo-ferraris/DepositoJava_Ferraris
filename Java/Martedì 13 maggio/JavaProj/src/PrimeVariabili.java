public class PrimeVariabili {
    public static void main(String[] args) {
        int provaNumero = 12; // Solo interi
        final String provaTesto = "Ciao Mondo"; // Solo stringhe, immutabile
        boolean provaBooleano = true; // Solo booleani: true/false

        int x = 3, y = 4, z = 5; // Dichiarazione multipla

        double pi = 3.14d; // Solo numeri decimali

        System.out.println(provaNumero + " " + provaTesto + " " + provaBooleano + x + y + z); // Stampa combinata
        System.out.println(pi);
    }
}